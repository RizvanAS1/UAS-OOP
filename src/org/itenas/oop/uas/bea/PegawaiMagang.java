package org.itenas.oop.bea;


public class PegawaiMagang extends Pegawai {
	String IDPM;
	String Nama_PegawaiM;
	String Divisi;
	int Masa_Kerja;
	String Status;
	int Gaji_Pokok;
	int Lama_Bekerja;
	String Tanggal_Masuk;
	String Tanggal_Keluar;
	
	public String getIDPM() {
		return IDPM;
	}
	public void setIDPM(String iDPM) {
		IDPM = iDPM;
	}
	public String getNama_PegawaiM() {
		return Nama_PegawaiM;
	}
	public void setNama_PegawaiM(String nama_PegawaiM) {
		Nama_PegawaiM = nama_PegawaiM;
	}
	public String getDivisi() {
		return Divisi;
	}
	public void setDivisi(String divisi) {
		Divisi = divisi;
	}
	public int getMasa_Kerja() {
		return Masa_Kerja;
	}
	public void setMasa_Kerja(int masa_Kerja) {
		Masa_Kerja = masa_Kerja;
	}
	public String getStatus() {
		return Status;
	}
	public void setStatus(String status) {
		Status = status;
	}
	public int getGaji_Pokok() {
		return Gaji_Pokok;
	}
	public void setGaji_Pokok(int gaji_Pokok) {
		Gaji_Pokok = gaji_Pokok;
	}
	public int getLama_Bekerja() {
		return Lama_Bekerja;
	}
	public void setLama_Bekerja(int lama_Bekerja) {
		Lama_Bekerja = lama_Bekerja;
	}
	public String getTanggal_Masuk() {
		return Tanggal_Masuk;
	}
	public void setTanggal_Masuk(String tanggal_Masuk) {
		Tanggal_Masuk = tanggal_Masuk;
	}
	public String getTanggal_keluar() {
		return Tanggal_Keluar;
	}
	public void setTanggal_keluar(String tanggal_keluar) {
		Tanggal_Keluar = tanggal_keluar;
	}
	
}
