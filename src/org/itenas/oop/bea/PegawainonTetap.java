package org.itenas.oop.bea;

public class PegawainonTetap extends Pegawai{
	String IDPnT;
	String Nama_Pegawai;
	String Level;
	int Masa_Kerja;
	String Status;
	int Gaji_Pokok;
	int Jumlah_Lembur;
	int Jumlah_Cuti;
	int Jumlah_Absen;
	int Jumlah_Dinas;
	
	public String getIDPnT() {
		return IDPnT;
	}
	public void setIDPnT(String iDPnT) {
		IDPnT = iDPnT;
	}
	public String getNama_Pegawai() {
		return Nama_Pegawai;
	}
	public void setNama_Pegawai(String nama_Pegawai) {
		Nama_Pegawai = nama_Pegawai;
	}
	public String getLevel() {
		return Level;
	}
	public void setLevel(String level) {
		Level = level;
	}
	public int getMasa_Kerja() {
		return Masa_Kerja;
	}
	public void setMasa_Kerja(int masa_Kerja) {
		Masa_Kerja = masa_Kerja;
	}
	public String getStatus() {
		return Status;
	}
	public void setStatus(String status) {
		Status = status;
	}
	public int getJumlah_Cuti() {
		return Jumlah_Cuti;
	}
	public void setJumlah_Cuti(int jumlah_Cuti) {
		Jumlah_Cuti = jumlah_Cuti;
	}
	public int getJumlah_Absen() {
		return Jumlah_Absen;
	}
	public void setJumlah_Absen(int jumlah_Absen) {
		Jumlah_Absen = jumlah_Absen;
	}
	public int getJumlah_Dinas() {
		return Jumlah_Dinas;
	}
	public void setJumlah_Dinas(int jumlah_Dinas) {
		Jumlah_Dinas = jumlah_Dinas;
	}
	public int getGaji_Pokok() {
		return Gaji_Pokok;
	}
	public void setGaji_Pokok(int gaji_Pokok) {
		Gaji_Pokok = gaji_Pokok;
	}
	public int getJumlah_Lembur() {
		return Jumlah_Lembur;
	}
	public void setJumlah_Lembur(int jumlah_Lembur) {
		Jumlah_Lembur = jumlah_Lembur;
	}
}
